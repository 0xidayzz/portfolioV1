export function findIconByName(name) {
  return NAME_TO_ICON[name.toLowerCase().trim()] || null;
}

export function slugify(name) {
  return name.toLowerCase()
    .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, "-")
    .replace(/[^a-z0-9-]/g, "");
}